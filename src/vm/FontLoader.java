package vm;

import vm.exceptions.FontLoaderException;
import vm.exceptions.MemoryWriteException;

public class FontLoader {
    private static final int MEMORY_OFFSET = 0x50;
    private static final int[] FONTSET = {
            0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
            0x20, 0x60, 0x20, 0x20, 0x70, // 1
            0xF0, 0x10, 0xF0, 0x80, 0xF0, // 2
            0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
            0x90, 0X90, 0xF0, 0x10, 0x10, // 4
            0xF0, 0x80, 0xF0, 0x10, 0xF0, // 5
            0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
            0xF0, 0x10, 0x20, 0x40, 0x40, // 7
            0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
            0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
            0xF0, 0x90, 0xF0, 0x90, 0x90, // A
            0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
            0xF0, 0x80, 0x80, 0x80, 0xF0, // C
            0xE0, 0x90, 0x90, 0x90, 0xE0, // D
            0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
            0xF0, 0x80, 0xF0, 0x80, 0x80  // F
    };

    private Memory memory;


    public FontLoader(Memory memory) {
        this.memory = memory;
    }

    public void loadFont() throws FontLoaderException {
        for (int i = 0; i < FONTSET.length; i++) {
            int address = MEMORY_OFFSET + i;
            char value = (char) FONTSET[i];

            try {
                memory.writeByte(address, value);
            } catch (MemoryWriteException e) {
               throw new FontLoaderException("Attempt to write font data to the wrong memory address.");
            }
        }
    }
}
